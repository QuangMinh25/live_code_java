package learnJava;

public class Variable {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(c);

        String name = "John";
        int age = 20;
        System.out.println(name);
        System.out.println(age);
    }
}
